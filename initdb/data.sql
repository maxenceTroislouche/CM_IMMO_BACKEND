DROP PROCEDURE IF EXISTS insert_bail_data;
CREATE PROCEDURE insert_bail_data(IN num_inserts INT)
BEGIN
    DECLARE i INT DEFAULT 0;
    DECLARE v_date_debut_bail DATE;
    DECLARE v_date_signature DATE;

    WHILE i < num_inserts DO
        -- Générer une date de début de bail
        SET v_date_debut_bail  = (DATE_ADD(DATE('1999-01-01'), INTERVAL ROUND(RAND() * DATEDIFF(CURDATE(), '1999-01-01')) DAY));

        -- Soustraire 3 jours de la date de début du bail pour obtenir la date de signature
        SET v_date_signature = (DATE_ADD(v_date_debut_bail, INTERVAL -3 DAY));

        -- Insérer les données dans la table bail
        INSERT INTO bail (id_bien, id_locataire, id_proprietaire, id_agent, date_debut_bail, date_signature)
        VALUES
        (
            (SELECT id FROM bien ORDER BY RAND() LIMIT 1),
            (SELECT id FROM tiers WHERE `id_type_tiers` = 2 ORDER BY RAND() LIMIT 1),
            (SELECT id FROM tiers WHERE `id_type_tiers` = 1 ORDER BY RAND() LIMIT 1),
            (SELECT id FROM tiers WHERE `id_type_tiers` = 3 ORDER BY RAND() LIMIT 1),
            v_date_debut_bail,
            v_date_signature
        );

        SET i = i + 1;
    END WHILE;

    COMMIT;
END //

