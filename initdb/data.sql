CREATE PROCEDURE insert_tiers_data(IN num_inserts INT)
BEGIN
    DECLARE i INT DEFAULT 0;

    DECLARE annee_naissance VARCHAR(2);
    DECLARE mois_naissance VARCHAR(2);
    DECLARE date_naissance DATE;

    DECLARE num_secu VARCHAR(20);
    DECLARE rib VARCHAR(25);

    DECLARE nom_temp VARCHAR(60);
    DECLARE prenom_temp VARCHAR(60);

    DECLARE id_type_tiers INT;

    WHILE i < num_inserts DO
        -- Générer une date de naissance aléatoire entre 1930 et aujourd'hui
        SET date_naissance = DATE_ADD(DATE('1930-01-01'), INTERVAL ROUND(RAND() * DATEDIFF(CURDATE(), '1930-01-01')) DAY);

        -- Générer le numéro de sécurité sociale
        SET annee_naissance = (SELECT DATE_FORMAT(date_naissance, '%y'));
        SET mois_naissance = (SELECT DATE_FORMAT(date_naissance, '%m'));

        SET num_secu = (SELECT CONCAT(ROUND(RAND() * 2),annee_naissance, mois_naissance, ROUND(RAND() * 9999999999)));

        -- Générer le RIB
        SET rib = (SELECT CONCAT('FR',ROUND(RAND() * 99999999999999999999)));

        -- Générer l'id_type_tiers avec les probabilités
        SET id_type_tiers = CASE
            WHEN RAND() < 0.75 THEN 1
            WHEN RAND() < 0.95 THEN 2
            ELSE 3
        END;

        -- Sélectionner un nom aléatoire de la table temp_noms
        SELECT nom INTO nom_temp FROM temp_noms ORDER BY RAND() LIMIT 1;
        -- Sélectionner un prénom aléatoire de la table temp_prenoms
        SELECT prenom INTO prenom_temp FROM temp_prenoms ORDER BY RAND() LIMIT 1;

        -- Effectuer l'insertion dans la table tiers
        INSERT INTO tiers (nom, prenom, date_de_naissance, numero_securite_sociale, rib, id_type_tiers)
        VALUES (nom_temp, prenom_temp, date_naissance, num_secu, rib, id_type_tiers);

        SET i = i + 1;
    END WHILE;
END $$

DELIMITER ;

